#Program header - Converting Degrees Fahrenheit into Degrees Kelvin
print("Converting Degrees Fahrenheit into Degrees Kelvin\n")
print("Program enhanced by Brian Sheehan")
#Input values (Fahrenheit degrees)
fltFahrenheit = 32 #Boiling water temperature
print("Input (Fahrenheit) : %.2f" % fltFahrenheit)
#Processing-Calculating Kelvin
fltKelvin = ((fltFahrenheit+459.67)*(5/9))
print("Processing-Calculating Kelvin = (fltFahrenheit+459.67)*(5/9)")
#Output(s)
print("Kelvin = %.2f" % fltKelvin)
